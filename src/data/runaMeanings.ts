// Funciones auxiliares para trabajar con las runas expandidas
export const getRunaById = (id: string): RunaMeaning | undefined => {
  return runaMeaningsExpanded.find(runa => runa.id === id);
};

export const getRunasByElement = (elemento: string): RunaMeaning[] => {
  return runaMeaningsExpanded.filter(runa => runa.elementoAsociado.includes(elemento));
};

export const getRunasByDios = (nombreDios: string): RunaMeaning[] => {
  return runaMeaningsExpanded.filter(runa => 
    runa.dioses.some(dios => dios.toLowerCase().includes(nombreDios.toLowerCase()))
  );
};

export const getRunasConSignificadoInvertido = (): RunaMeaning[] => {
  return runaMeaningsExpanded.filter(runa => runa.significadoInvertido);
};

export const getRunasByPalabraClave = (palabra: string): RunaMeaning[] => {
  return runaMeaningsExpanded.filter(runa => 
    runa.palabrasClave.some(clave => 
      clave.toLowerCase().includes(palabra.toLowerCase())
    )
  );
};

export const getRunasAfines = (runaId: string): RunaMeaning[] => {
  const runa = getRunaById(runaId);
  if (!runa) return [];
  
  return runa.afinidadOtrasRunas
    .map(id => getRunaById(id))
    .filter(r => r !== undefined) as RunaMeaning[];
};